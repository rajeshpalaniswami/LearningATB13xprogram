package ex_09_Switch;

public class Lab089_Interview {
    public static void main(String[] args) {
        char ch = 'b';
        switch (ch){

            case 65:
                System.out.println("Match ASCII");
                break;
            case 'b':
                System.out.println("print b");
                break;
            default:
                System.out.println("No Match");


        }
    }
}
